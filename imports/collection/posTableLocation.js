export const Pos_TableLocation = new Mongo.Collection('pos_tableLocation');

Pos_TableLocation.schema = new SimpleSchema({
    name: {
        type: String,
        label: "Name",
        index: true
    },
    khName: {
        type: String,
        label: "Khmer Name",
        optional: true
    },
    description: {
        type: String,
        label: "Description",
        optional: true
    },
    createdAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return moment().toDate();
            }
        }
    },
    updatedAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return moment().toDate();
            }
        }
    },
    createdUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return Meteor.userId();
            }
        }
    },
    updatedUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return Meteor.userId();
            }
        }
    }
});

Pos_TableLocation.attachSchema(Pos_TableLocation.schema);

